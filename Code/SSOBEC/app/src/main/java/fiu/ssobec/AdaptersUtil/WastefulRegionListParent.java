public class WastefulRegionListParent {
    private String name;
    private String light_description;
    private String icon;
    private String plugload;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getLightDescription() {
        return light_description;
    }

    public void setLightDescription(String light_description) {
        this.light_description = light_description;
    }

    public String getIcon() {
        return icon;
    }

    public void setIcon(String icon) {
        this.icon = icon;
    }

    public String getPlugload() {
        return plugload;
    }

    public void setPlugload(String plugload) {
        this.plugload = plugload;
    }

}
